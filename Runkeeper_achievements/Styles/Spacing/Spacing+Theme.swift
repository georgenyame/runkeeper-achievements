    static let spacingEighth: CGFloat = 2
    static let spacingQuarter: CGFloat = 4
    static let spacingHalf: CGFloat = 8
    static let spacingThreeQuarters: CGFloat = 12
    static let spacingOne: CGFloat = 16
    static let spacingOneAndQuarter: CGFloat = 20
    static let spacingOneAndHalf: CGFloat = 24
    static let spacingDouble: CGFloat = 32
}
